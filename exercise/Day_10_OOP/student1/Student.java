package student1;

public class Student {
	private int id;
	private String name;
	private int kor;
	private int eng;
	private int math;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getKor() {
		return kor;
	}
	public void setKor(int kor) {
		this.kor = kor;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}
	
	public int getsum() {
		return this.kor + this.eng + this.math;
	}
	public int getAvg() {
		return getsum()/3;
	}
}
